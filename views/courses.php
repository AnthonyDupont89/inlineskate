    <h2>Cours</h2>
    <div class="content">
        <p>
            L'objectif de nos cours est d'acquérir les techniques et l'assurance
            nécessaire pour rouler en milieu urbain en toute sécurité. <br><br>

            Nous proposons un processus d'apprentissage qui te permettra progressivement
            à maitriser ton corps et tes mouvements dans l'espace, à contrôler ta vitesse
            et ainsi être capable de bien choisir tes trajectoires dans un environnement routier
            avec du traffic et un code de la route à respecter. <br><br>

            Ça peut paraître intimidant au début, mais ne t'inquiète pas, à l'aide de nos moniteurs et
            avec de la pratique, te déplacer à roller deviendra aussi naturel que de te prommener à pied.
            Nous t'amènerons donc, étapes par étape, à avoir confiance en toi et à te déplacer avec aisance
            dans cette jungle urbaine. <br><br>

            Pour ce faire, nous travaillerons ta coordination et nous ferons du renforcement musculaire pour améliorer ta dextérité en roller
            (et oui, c'est du sport) et nous t'apprendrons à accélérer, à ralentir, à tourner et à freiner en toute sérénité.
        </p>
